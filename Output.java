public class Output {

	private short value; // Instance variable to hold the output value

	/******************************************************************************************
	 * Default Output Constructor
	 *****************************************************************************************/
	public Output() {

	}
	// ---------------------------------------------------------------------------------------/
	
	/******************************************************************************************
	 * Utility Method - print (short n)
	 *****************************************************************************************/
	public void print(short n) {
		value = n;
	}
	// ---------------------------------------------------------------------------------------/
	
	/******************************************************************************************
	 * toString override (Output)
	 *****************************************************************************************/
	public String toString() {
		String s = new String();
		s += "Output : " + value;
		return s;

	}
	// ---------------------------------------------------------------------------------------/
}
