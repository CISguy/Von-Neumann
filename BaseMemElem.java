public class BaseMemElem {

	private short value; // Instance variable used to hold the value inside a base memory element

	/******************************************************************************************
	 * Default Base Memory Element Constructor (BaseMemElem)
	 *****************************************************************************************/
	public BaseMemElem() {

	}
	// ---------------------------------------------------------------------------------------/

	/******************************************************************************************
	 * Utility Methods (write, read)
	 *****************************************************************************************/
	public short write(short i) {
		return value = i;
	}

	public short read() {
		return value;
	}
	// ---------------------------------------------------------------------------------------/

	/*****************************************************************************************
	 * toString override (BaseMemElem)
	 ****************************************************************************************/
	public String toString() {
		String s = new String();
		s += read();
		return s;
	}
	// ---------------------------------------------------------------------------------------/
}
